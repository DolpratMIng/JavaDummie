package TryItOut;

public class T3 {
    public static void main(String[] args) {
        int i = 10;

        i += 2; // 10+2 = 12
        i -= 5; // 12 - 5 = 7
        i *= 6; // 7*6 = 42

        System.out.println(i); // 42
        System.out.println(i += 3); // 45
        System.out.println(i /=2); // 45/2 = 22
    }
}
